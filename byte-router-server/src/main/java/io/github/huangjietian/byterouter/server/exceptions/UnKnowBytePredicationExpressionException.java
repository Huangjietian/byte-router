public class UnKnowBytePredicationExpressionException extends Exception {

    public UnKnowBytePredicationExpressionException() {
    }

    public UnKnowBytePredicationExpressionException(String message) {
        super(message);
    }

    public UnKnowBytePredicationExpressionException(String message, Throwable cause) {
        super(message, cause);
    }

    public UnKnowBytePredicationExpressionException(Throwable cause) {
        super(cause);
    }

    public UnKnowBytePredicationExpressionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
